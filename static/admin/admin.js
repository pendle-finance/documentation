// ─── Sidebar JS writer ────────────────────────────────────────────────────────
function sectionOrderToJs(nodes, indent) {
  var pad = ' '.repeat(indent);
  return nodes.map(function(node) {
    if (node.type === 'doc') {
      var s = pad + '{ type: "doc", id: "' + node.id.replace(/"/g, '\\"') + '"';
      if (node.label) s += ', label: "' + node.label.replace(/"/g, '\\"') + '"';
      if (node.icon) s += ', customProps: { icon: "' + node.icon.replace(/"/g, '\\"') + '" }';
      s += ' }';
      return s;
    } else if (node.type === 'category') {
      var s = pad + '{\n';
      s += pad + '  type: "category",\n';
      s += pad + '  label: "' + node.label.replace(/"/g, '\\"') + '",\n';
      if (node.icon) s += pad + '  customProps: { icon: "' + node.icon.replace(/"/g, '\\"') + '" },\n';
      s += pad + '  items: [\n';
      s += sectionOrderToJs(node.children || [], indent + 4) + '\n';
      s += pad + '  ],\n';
      s += pad + '}';
      return s;
    } else if (node.type === 'link') {
      var s = pad + '{ type: "link", label: "' + node.label.replace(/"/g, '\\"') + '", href: "' + node.href + '"';
      if (node.icon) s += ', customProps: { icon: "' + node.icon.replace(/"/g, '\\"') + '" }';
      s += ' }';
      return s;
    }
    return '';
  }).filter(Boolean).join(',\n');
}

