// Helper: find and remove a node (any type) by object reference
function removeNodeByRef(list, ref) {
  for (var i = 0; i < list.length; i++) {
    if (list[i] === ref) { list.splice(i, 1); return true; }
    if (list[i].type === 'category' && removeNodeByRef(list[i].children || [], ref)) return true;
  }
  return false;
}

// Helper: find parent list of a node by object reference
function findParentOfNode(list, ref) {
  for (var i = 0; i < list.length; i++) {
    if (list[i] === ref) return list;
    if (list[i].type === 'category') {
      var found = findParentOfNode(list[i].children || [], ref);
      if (found) return found;
    }
  }
  return null;
}

