function onDrop(e) {
  e.preventDefault();
  e.currentTarget.classList.remove('drag-over');
  const targetId = e.currentTarget.dataset.id;
  if (!dragSrcId || dragSrcId === targetId) { dragSrcId = null; dragSrcPath = null; return; }

  // Find both nodes in sectionOrder — reorder regardless of physical folder
  const srcParentList = findParentChildren(sectionOrder, dragSrcId);
  const tgtParentList = findParentChildren(sectionOrder, targetId);

  if (srcParentList && tgtParentList) {
    const srcNode = removeDocFromOrder(sectionOrder, dragSrcId);
    if (srcNode) {
      const tgtIdx = tgtParentList.findIndex(function(n) { return n.type === 'doc' && n.id === targetId; });
      tgtParentList.splice(tgtIdx, 0, srcNode);
      markSidebarDirty();
      renderSidebar();
    }
  }
  dragSrcId = null;
  dragSrcPath = null;
}


