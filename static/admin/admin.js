(async () => {
  const wasCallback = handleOAuthCallback();
  if (wasCallback) return; // login screen already shown with error
  const authed = await checkAuth();
  if (authed === true) {
    bootApp();
  } else if (authed === 'denied') {
    // showAccessDenied already called inside checkAuth
  } else {
    showLoginScreen();
  }
})();
