function startOAuth() {
  // Store a random state value to verify the callback
  const state = Math.random().toString(36).slice(2);
  sessionStorage.setItem('oauth-state', state);
  const params = new URLSearchParams({
    client_id: GH_CLIENT_ID,
    scope: 'repo',
    state,
    redirect_uri: OAUTH_FUNCTION_URL,
    prompt: 'select_account',
  });
  location.href = 'https://github.com/login/oauth/authorize?' + params;
}

// Called on page load — checks if we're returning from OAuth callback
function handleOAuthCallback() {
  const hash = location.hash;
  if (!hash) return false;
  const params = new URLSearchParams(hash.slice(1));
  const token = params.get('token');
  const error = params.get('error');
  // Clear hash from URL
  history.replaceState(null, '', location.pathname);
  if (error) {
    showLoginScreen();
    const el = document.getElementById('login-error');
    el.textContent = 'Login failed: ' + decodeURIComponent(error);
    el.style.display = 'block';
    return true;
  }
  if (token) {
    saveGhToken(token);
    return false; // proceed to checkAuth normally
  }
  return false;
}

function logout() {
  if (!confirm('Log out? You will need to log in again to make edits.')) return;
  clearGhToken();
  location.reload();
}

async function checkAuth() {
  if (IS_LOCAL) return true; // local proxy — no auth needed
  const token = getGhToken();
  if (!token) return false;
  try {
    const user = await ghFetch('/user');
    // Check write access by fetching repo metadata — returns permissions.push for the authed user
    const repo = await ghFetch('/repos/' + GH_REPO);
    const canWrite = repo.permissions && (repo.permissions.push || repo.permissions.admin);
    if (!canWrite) {
      showAccessDenied(user.login);
      return 'denied';
    }
    setLogoutButton(user.login, user.avatar_url);
    return true;
  } catch(e) {
    clearGhToken();
    return false;
  }
}

