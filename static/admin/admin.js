// Build flat list of folder paths from sectionOrder doc ids and sectionFiles
function collectFolders() {
  const folders = new Set();
  folders.add('');
  // Derive folders from doc ids in sectionOrder
  function walkOrder(nodes) {
    for (const node of nodes) {
      if (node.type === 'doc') {
        const parts = node.id.split('/');
        parts.pop();
        if (parts.length > 0) folders.add(parts.join('/'));
      }
      if (node.type === 'category') {
        walkOrder(node.children || []);
      }
    }
  }
  walkOrder(sectionOrder);
  // Also include folders from sectionFiles (for the move modal)
  Object.keys(sectionFiles).forEach(function(p) {
    const r = p.replace(currentSection.folder + '/', '');
    const ps = r.split('/');
    ps.pop();
    if (ps.length > 0) folders.add(ps.join('/'));
  });
  return folders;
}

