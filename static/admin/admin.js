function findFirstDocNode(nodes) {
  for (const node of nodes) {
    if (node.type === 'doc') return node;
    if (node.type === 'category' && node.children) {
      const found = findFirstDocNode(node.children);
      if (found) return found;
    }
  }
  return null;
}

// ─── Sidebar JS loader ────────────────────────────────────────────────────────
function parseSidebarsJs(raw) {
  // Find the opening bracket of the sidebar array
  var keyMatch = raw.match(/module\.exports\s*=\s*\{[^:]+:\s*(\[)/);
  if (!keyMatch) return [];
  var start = raw.indexOf('[', keyMatch.index + keyMatch[0].length - 1);
  // Walk brackets to find the matching closing bracket
  var depth = 0, i = start, inStr = false, strChar = '';
  for (; i < raw.length; i++) {
    var c = raw[i];
    if (inStr) {
      if (c === '\\') { i++; continue; }
      if (c === strChar) inStr = false;
    } else if (c === '"' || c === "'" || c === '`') {
      inStr = true; strChar = c;
    } else if (c === '[' || c === '{') {
      depth++;
    } else if (c === ']' || c === '}') {
      depth--;
      if (depth === 0) break;
    }
  }
  var arrayStr = raw.slice(start, i + 1);
  try {
    return (new Function('return ' + arrayStr))();
  } catch(e) {
    console.warn('parseSidebarsJs error:', e);
    return [];
  }
}

function parsedItemsToSectionOrder(items) {
  return items.map(function(item) {
    if (item.type === 'doc') {
      return {
        type: 'doc',
        id: item.id,
        label: item.label || item.id.split('/').pop(),
        icon: (item.customProps && item.customProps.icon) || '',
      };
    } else if (item.type === 'category') {
      return {
        type: 'category',
        label: item.label || '',
        icon: (item.customProps && item.customProps.icon) || '',
        children: parsedItemsToSectionOrder(item.items || []),
      };
    } else if (item.type === 'link') {
      return {
        type: 'link',
        label: item.label || '',
        href: item.href || '',
        icon: (item.customProps && item.customProps.icon) || '',
      };
    }
    return null;
  }).filter(Boolean);
}

async function loadSidebarJs(section) {
  sectionOrder = [];
  try {
    // CN sections share the EN sidebars.js — use sidebarFolder if present
    var sidebarFolder = section.sidebarFolder || section.folder;
    var res = await proxyCall('getEntry', { path: sidebarFolder + '/sidebars.js', branch: BRANCH });
    var raw = res.data || '';
    if (!raw) { sectionOrder = []; return; }
    var parsed = parseSidebarsJs(raw);
    sectionOrder = parsedItemsToSectionOrder(parsed);
  } catch(e) {
    console.warn('loadSidebarJs failed:', e.message);
    sectionOrder = [];
  }
}

// ─── File path resolver ───────────────────────────────────────────────────────
function getFilePath(id) {
  var primary = currentSection.folder + '/' + id + '.' + currentSection.extension;
  var alt = currentSection.folder + '/' + id + (currentSection.extension === 'md' ? '.mdx' : '.md');
  if (sectionFiles[primary]) return primary;
  if (sectionFiles[alt]) return alt;
  return primary;
}

// ─── Section file loader ──────────────────────────────────────────────────────
async function loadSectionFiles(section) {
  sectionFiles = {};
  const extensions = section.extension === 'md' ? ['md', 'mdx'] : ['mdx', 'md'];
  for (const ext of extensions) {
    try {
      const res = await proxyCall('entriesByFolder', {
        folder: section.folder,
        extension: ext,
        depth: 10,
        branch: BRANCH,
      });
      const files = Array.isArray(res) ? res : (res.entries || res.data || []);
      files.forEach(f => {
        const p = (f.file && f.file.path) || f.path || (typeof f === 'string' ? f : null);
        if (!p) return;
        const raw = (f.data != null) ? f.data : '';
        const fm = raw ? parseFrontmatter(raw).frontmatter : {};
        sectionFiles[p] = { title: fm.title || '' };
      });
    } catch(e) {
      console.warn('loadSectionFiles failed for .' + ext + ':', e.message);
    }
  }
}


